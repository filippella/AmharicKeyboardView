package org.dalol.amharickeyboardlibrary.model.layoutmanager.properties;

import java.util.ArrayList;
import java.util.List;

/**
 * @author Filippo Engidashet <filippo.eng@gmail.com>
 * @version 1.0.0
 * @since 9/30/2016
 */
public class KeyButton {

    public static final int DEFAULT_ROW_COUNT = 1;
    public static final int DEFAULT_COLUMN_COUNT = 1;

    private int rowCount;
    private int columnCount;
    private int left;
    private int top;

    private List<KeyButton> displayButtons = new ArrayList<>();

    public KeyButton() {
        this.rowCount = DEFAULT_ROW_COUNT;
        this.columnCount = DEFAULT_COLUMN_COUNT;
    }

    public KeyButton(int rowCount, int columnCount) {
        this.rowCount = rowCount;
        this.columnCount = columnCount;
    }

    public int getRowCount() {
        return rowCount;
    }

    public int getColumnCount() {
        return columnCount;
    }

    public int getLeft() {
        return left;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }
}
